var mongoose = require('mongoose');
var Admin = new mongoose.Schema({
  email: {
    type: String,
    unique: true,
    required: true,
    trim: true
  },
  username: {
    type: String,
    unique: true,
    required: true,
    trim: true
  },
  password: {
    type: String,
    required: true,
  }
});
var Admin = mongoose.model('Admin', Admin);
module.exports = Admin;